package kr.co.netpoint.vo.project;

public class TbProjectVO {
	private int CProjectId;
	private String CPCode;
	private String CPNo;
	private String CPName;
	private String CPEx;

	public int getCProjectId() {
		return CProjectId;
	}
	public void setCProjectId(int cProjectId) {
		CProjectId = cProjectId;
	}
	public String getCPCode() {
		return CPCode;
	}
	public void setCPCode(String cPCode) {
		CPCode = cPCode;
	}
	public String getCPNo() {
		return CPNo;
	}
	public void setCPNo(String cPNo) {
		CPNo = cPNo;
	}
	public String getCPName() {
		return CPName;
	}
	public void setCPName(String cPName) {
		CPName = cPName;
	}
	public String getCPEx() {
		return CPEx;
	}
	public void setCPEx(String cPEx) {
		CPEx = cPEx;
	}

}
